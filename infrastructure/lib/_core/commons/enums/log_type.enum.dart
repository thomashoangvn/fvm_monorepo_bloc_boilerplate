/// Enum representing different types of log messages.
enum LogTypeEnum {
  /// Debugging information.
  debug,

  /// Error messages.
  error,

  /// Informational messages.
  info,

  /// Verbose log messages, used for detailed debugging.
  verbose,

  /// Warning messages.
  warning,
}
